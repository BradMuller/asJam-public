/* -----[ helper for AST traversal ]----- */

function ast_walker(ast) {
        function _vardefs(defs) {
                return [ this[0], MAP(defs, function(def){
                        var a = [ def[0] ];
                        if (def.length > 1)
                                a[1] = walk(def[1]);
                        return a;
                }) ];
        };
        function _block(statements) {
                var out = [ this[0] ];
                if (statements != null)
                        out.push(MAP(statements, walk));
                return out;
        };
        var walkers = {
                "string": function(str) {
                        return [ this[0], str ];
                },
                "num": function(num) {
                        return [ this[0], num ];
                },
                "name": function(name) {
                        return [ this[0], name ];
                },
                "toplevel": function(statements) {
                        return [ this[0], MAP(statements, walk) ];
                },
                "block": _block,
                "splice": _block,
                "var": _vardefs,
                "const": _vardefs,
                "try": function(t, c, f) {
                        return [
                                this[0],
                                MAP(t, walk),
                                c != null ? [ c[0], MAP(c[1], walk) ] : null,
                                f != null ? MAP(f, walk) : null
                        ];
                },
                "throw": function(expr) {
                        return [ this[0], walk(expr) ];
                },
                "new": function(ctor, args) {
                        return [ this[0], walk(ctor), MAP(args, walk) ];
                },
                "switch": function(expr, body) {
                        return [ this[0], walk(expr), MAP(body, function(branch){
                                return [ branch[0] ? walk(branch[0]) : null,
                                         MAP(branch[1], walk) ];
                        }) ];
                },
                "break": function(label) {
                        return [ this[0], label ];
                },
                "continue": function(label) {
                        return [ this[0], label ];
                },
                "conditional": function(cond, t, e) {
                        return [ this[0], walk(cond), walk(t), walk(e) ];
                },
                "assign": function(op, lvalue, rvalue) {
                        return [ this[0], op, walk(lvalue), walk(rvalue) ];
                },
                "dot": function(expr) {
                        return [ this[0], walk(expr) ].concat(slice(arguments, 1));
                },
                "call": function(expr, args) {
                        return [ this[0], walk(expr), MAP(args, walk) ];
                },
                "function": function(name, args, body) {
                        return [ this[0], name, args.slice(), MAP(body, walk) ];
                },
                "defun": function(name, args, body) {
                        return [ this[0], name, args.slice(), MAP(body, walk) ];
                },
                "if": function(conditional, t, e) {
                        return [ this[0], walk(conditional), walk(t), walk(e) ];
                },
                "for": function(init, cond, step, block) {
                        return [ this[0], walk(init), walk(cond), walk(step), walk(block) ];
                },
                "for-in": function(vvar, key, hash, block) {
                        return [ this[0], walk(vvar), walk(key), walk(hash), walk(block) ];
                },
                "while": function(cond, block) {
                        return [ this[0], walk(cond), walk(block) ];
                },
                "do": function(cond, block) {
                        return [ this[0], walk(cond), walk(block) ];
                },
                "return": function(expr) {
                        return [ this[0], walk(expr) ];
                },
                "binary": function(op, left, right) {
                        return [ this[0], op, walk(left), walk(right) ];
                },
                "unary-prefix": function(op, expr) {
                        return [ this[0], op, walk(expr) ];
                },
                "unary-postfix": function(op, expr) {
                        return [ this[0], op, walk(expr) ];
                },
                "sub": function(expr, subscript) {
                        return [ this[0], walk(expr), walk(subscript) ];
                },
                "object": function(props) {
                        return [ this[0], MAP(props, function(p){
                                return p.length == 2
                                        ? [ p[0], walk(p[1]) ]
                                        : [ p[0], walk(p[1]), p[2] ]; // get/set-ter
                        }) ];
                },
                "regexp": function(rx, mods) {
                        return [ this[0], rx, mods ];
                },
                "array": function(elements) {
                        return [ this[0], MAP(elements, walk) ];
                },
                "stat": function(stat) {
                        return [ this[0], walk(stat) ];
                },
                "seq": function() {
                        return [ this[0] ].concat(MAP(slice(arguments), walk));
                },
                "label": function(name, block) {
                        return [ this[0], name, walk(block) ];
                },
                "with": function(expr, block) {
                        return [ this[0], walk(expr), walk(block) ];
                },
                "atom": function(name) {
                        return [ this[0], name ];
                },
                "splat": function() {
                        return [ this[0] ];
                }
        };

        var user = {};
        var stack = [];
        function walk(ast) {
                if (ast == null)
                        return null;
                try {
                        stack.push(ast);
                        var type = ast[0];
                        var gen = user[type];
                        if (gen) {
                                var ret = gen.apply(ast, ast.slice(1));
                                if (ret != null)
                                        return ret;
                        }
                        gen = walkers[type];
                        return gen.apply(ast, ast.slice(1));
                } finally {
                        stack.pop();
                }
        };

        function with_walkers(walkers, cont){
                var save = {}, i;
                for (i in walkers) if (HOP(walkers, i)) {
                        save[i] = user[i];
                        user[i] = walkers[i];
                }
                var ret = cont();
                for (i in save) if (HOP(save, i)) {
                        if (!save[i]) delete user[i];
                        else user[i] = save[i];
                }
                return ret;
        };

        return {
                walk: walk,
                with_walkers: with_walkers,
                parent: function() {
                        return stack[stack.length - 2]; // last one is current node
                },
                stack: function() {
                        return stack;
                }
        };
};

/* -----[ Scope and mangling ]----- */

function Scope(parent) {
        this.names = {};        // names defined in this scope
        this.mangled = {};      // mangled names (orig.name => mangled)
        this.rev_mangled = {};  // reverse lookup (mangled => orig.name)
        this.cname = -1;        // current mangled name
        this.refs = {};         // names referenced from this scope
        this.uses_with = false; // will become TRUE if with() is detected in this or any subscopes
        this.uses_eval = false; // will become TRUE if eval() is detected in this or any subscopes
        this.parent = parent;   // parent scope
        this.children = [];     // sub-scopes
        if (parent) {
                this.level = parent.level + 1;
                parent.children.push(this);
        } else {
                this.level = 0;
        }
};

var base54 = (function(){
        var DIGITS = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ$_";
        return function(num) {
                var ret = "";
                do {
                        ret = DIGITS.charAt(num % 54) + ret;
                        num = Math.floor(num / 54);
                } while (num > 0);
                return ret;
        };
})();

Scope.prototype = {
        has: function(name) {
                for (var s = this; s; s = s.parent)
                        if (HOP(s.names, name))
                                return s;
        },
        has_mangled: function(mname) {
                for (var s = this; s; s = s.parent)
                        if (HOP(s.rev_mangled, mname))
                                return s;
        },
        toJSON: function() {
                return {
                        names: this.names,
                        uses_eval: this.uses_eval,
                        uses_with: this.uses_with
                };
        },

        get_new_name: function(baseName) {
                // we must be careful that the new mangled name:
                //
                // 1. doesn't shadow a mangled name from a parent
                //    scope, unless we don't reference the original
                //    name from this scope OR from any sub-scopes!
                //    This will get slow.
                //
                // 2. doesn't shadow an original name from a parent
                //    scope, in the event that the name is not mangled
                //    in the parent scope and we reference that name
                //    here OR IN ANY SUBSCOPES!
                //
                // 3. doesn't shadow a name that is referenced but not
                //    defined (possibly global defined elsewhere).
                var counter = -1;

                for (;;) {
                        var m = baseName + base54(++counter), prior;

                        // case 1.
                        prior = this.has_mangled(m);
                        if (prior && this.refs[prior.rev_mangled[m]] === prior)
                                continue;

                        // case 2.
                        prior = this.has(m);
                        if (prior && prior !== this && this.refs[m] === prior && !prior.has_mangled(m))
                                continue;

                        // case 3.
                        if (HOP(this.refs, m) && this.refs[m] == null)
                                continue;

                        // I got "do" once. :-/
                        if (!is_identifier(m))
                                continue;

                        return m;
                }
        },
        get_free_name: function(baseName){
                if (!baseName) {
                        return this.get_new_name();
                }

                if (!this.has(baseName)) return baseName;
                return this.get_new_name(baseName);
        },
        introduce_name: function(baseName) {
                if (this.uses_eval || this.uses_with) /* TODO Warning */;

                var name = this.get_free_name(baseName);
                return this.define(name);
        },
        rewrite_name: function(curName, newName) {
                if (curName == newName) return curName;
                if (this.uses_eval || this.uses_with) /* TODO Warning */;
                var s = this.has(curName);
                if (!s) {
                        // curName isn't named
                        if (newName) {
                                if (this.has(newName)) return s;
                        } else {
                                return curName;
                        }
                }

                var name = this.get_free_name(newName || curName);
                if (HOP(s.mangled, curName)) return s.mangled[curName]; // already mangled in this scope

                s.rev_mangled[name] = curName;
                return s.mangled[curName] = name;
        },
        get_mangled: function(name, newMangle) {
                if (this.uses_eval || this.uses_with) return name; // no mangle if eval or with is in use
                var s = this.has(name);
                if (!s) return name; // not in visible scope, no mangle
                if (HOP(s.mangled, name)) return s.mangled[name]; // already mangled in this scope
                if (!newMangle) return name;                      // not found and no mangling requested

                var m = s.next_mangled();
                s.rev_mangled[m] = name;
                return s.mangled[name] = m;
        },
        define: function(name) {
                if (name != null)
                        return this.names[name] = name;
        },
        walk: function(callback) {
                if (callback(this) === false) {
                        return false;
                }

                for (var i = 0; i < this.children.length; ++i) {
                        if (this.children[i].walk(callback)) {
                                return false;
                        }
                }

                return true;
        }
};

function ast_add_scope(ast) {
        var current_scope = null;
        var w = ast_walker(), walk = w.walk;
        var having_eval = [];

        function with_new_scope(cont) {
                current_scope = new Scope(current_scope);
                var ret = current_scope.body = cont();
                ret.scope = current_scope;
                current_scope = current_scope.parent;
                return ret;
        };

        function define(name) {
                return current_scope.define(name);
        };

        function reference(name) {
                current_scope.refs[name] = true;
        };

        function _lambda(name, args, body) {
                var is_defun = this[0] == "defun";
                return [ this[0], is_defun ? define(name) : name, args, with_new_scope(function(){
                        if (!is_defun) define(name);
                        MAP(args, define);
                        return MAP(body, walk);
                })];
        };

        return with_new_scope(function(){
                // process AST
                var ret = w.with_walkers({
                        "function": _lambda,
                        "defun": _lambda,
                        "with": function(expr, block) {
                                for (var s = current_scope; s; s = s.parent)
                                        s.uses_with = true;
                        },
                        "var": function(defs) {
                                MAP(defs, function(d){ define(d[0]) });
                        },
                        "const": function(defs) {
                                MAP(defs, function(d){ define(d[0]) });
                        },
                        "try": function(t, c, f) {
                                if (c != null) return [
                                        this[0],
                                        MAP(t, walk),
                                        [ define(c[0]), MAP(c[1], walk) ],
                                        f != null ? MAP(f, walk) : null
                                ];
                        },
                        "name": function(name) {
                                if (name == "eval")
                                        having_eval.push(current_scope);
                                reference(name);
                        }
                }, function(){
                        return walk(ast);
                });

                // the reason why we need an additional pass here is
                // that names can be used prior to their definition.

                // scopes where eval was detected and their parents
                // are marked with uses_eval, unless they define the
                // "eval" name.
                MAP(having_eval, function(scope){
                        if (!scope.has("eval")) while (scope) {
                                scope.uses_eval = true;
                                scope = scope.parent;
                        }
                });

                // for referenced names it might be useful to know
                // their origin scope.  current_scope here is the
                // toplevel one.
                function fixrefs(scope, i) {
                        // do children first; order shouldn't matter
                        for (i = scope.children.length; --i >= 0;)
                                fixrefs(scope.children[i]);
                        for (i in scope.refs) if (HOP(scope.refs, i)) {
                                // find origin scope and propagate the reference to origin
                                for (var origin = scope.has(i), s = scope; s; s = s.parent) {
                                        s.refs[i] = origin;
                                        if (s === origin) break;
                                }
                        }
                };
                fixrefs(current_scope);

                return ret;
        });
};

function ast_add_scope_as3(ast, track_global_scope) {
        var current_scope = null;
        var w = ast_walker(), walk = w.walk;
        var having_eval = [];

        function with_new_scope(cont) {
                current_scope = new Scope(current_scope);
                var ret = current_scope.body = cont();
                ret.scope = current_scope;
                current_scope = current_scope.parent;
                return ret;
        };

        function define(name) {
                return current_scope.define(name);
        };

        function reference(name) {
                current_scope.refs[name] = true;
        };

        function define_typed_name(def) {
                define(def[0]);
        }

        function _lambda(name, args, body, returnType, modifiers) {
                var is_defun = this[0] == "defun";

                var newBody = with_new_scope(function(){
                        if (!is_defun) define(name);
                        MAP(args, define_typed_name);
                        return MAP(body, walk);
                });

                return [ this[0], is_defun ? define(name) : name, args, newBody, returnType, modifiers ];
        };

        function use_sp() {
                current_scope.refs.sp = current_scope;
        };

        return with_new_scope(function(){
                // process AST
                var ret = w.with_walkers({
                        "function": _lambda,
                        "defun": _lambda,
                        "with": function(expr, block) {
                                for (var s = current_scope; s; s = s.parent)
                                        s.uses_with = true;
                        },
                        "var": function(defs) {
                                return [ this[0], MAP(defs, function(def){
                                        define(def[0]);
                                        var a = [ def[0], def[1] ];
                                        if (def.length > 2)
                                                a[2] = walk(def[2]);
                                        return a;
                                }) ];
                        },
                        "const": function(defs) {
                                MAP(defs, define_typed_name);
                        },
                        "try": function(t, c, f) {
                                if (c != null) return [
                                        this[0],
                                        MAP(t, walk),
                                        [ define(c[0]), MAP(c[1], walk) ],
                                        f != null ? MAP(f, walk) : null
                                ];
                        },
                        "name": function(name) {
                                if (name == "eval")
                                        having_eval.push(current_scope);
                                reference(name);
                        },
                        "class": function(name, members) {
                                use_sp();

                                return with_new_scope(function(){
                                        current_scope.is_class = true;

                                        members = MAP(members, walk);
                                        MAP(members, function(member) {
                                                current_scope.define(member[1]);
                                        });

                                        return [ "class", name, members ];
                                });
                        },
                        "method": _lambda,
                        "binary": function(operator, lvalue, rvalue) {
                                if (operator == "is" || operator == "as") {
                                        use_sp();
                                }
                        }
                }, function(){
                        return walk(ast);
                });

                // the reason why we need an additional pass here is
                // that names can be used prior to their definition.

                // scopes where eval was detected and their parents
                // are marked with uses_eval, unless they define the
                // "eval" name.
                MAP(having_eval, function(scope){
                        if (!scope.has("eval")) while (scope) {
                                scope.uses_eval = true;
                                scope = scope.parent;
                        }
                });

                // for referenced names it might be useful to know
                // their origin scope.  current_scope here is the
                // toplevel one.
                function fixrefs(scope, i) {
                        // do children first; order shouldn't matter
                        for (i = scope.children.length; --i >= 0;)
                                fixrefs(scope.children[i]);
                        for (i in scope.refs) if (HOP(scope.refs, i)) {
                                // find origin scope and propagate the reference to origin
                                for (var origin = scope.has(i), s = scope; s; s = s.parent) {
                                        s.refs[i] = origin;
                                        if (s === origin) break;
                                }
                        }
                };
                fixrefs(current_scope);

                if (track_global_scope) {
                        ret.scope = current_scope;
                }

                return ret;
        });
};

/* -----[ rewrite AS3 as JS ]----- */

function rewrite(ast) {
        var w = ast_walker(), walk = w.walk, scope;

        var member_name_manglers = {
                "public": function(name) {
                        return name;
                },
                "protected": function(name) {
                        return "asJam_protected__" + name;
                },
                "private": function(name) {
                        // TODO mangle name based on class name
                        // to prevent clashing
                        return "asJam_private__" + name;
                }
        };

        function mangle_member_name(name, modifiers) {
                return modifiers.reduce(function (name, modifier) {
                        if (HOP(member_name_manglers, modifier)) {
                                return member_name_manglers[modifier](name);
                        }

                        return name;
                }, name);
        };

        function should_rewrite(name, ignoreKeywords) {
                if (!ignoreKeywords) {
                        if (name == "sp" || name == "self") return true;
                }
                if (scope.modifiers) return true;
                return false;
        };

        function rewrite(name, ignoreKeywords) {
                if (!ignoreKeywords) {
                        if (name == "sp" || name == "self") return scope.rewrite_name(name);
                }
                if (scope.modifiers) {
                        return scope.rewrite_name(name, mangle_member_name(name, scope.modifiers));
                }
                return name;
        };

        function get_rewritten(name, canRewrite, ignoreKeywords) {
                if (canRewrite && should_rewrite(name, ignoreKeywords)) {
                        return rewrite(name, ignoreKeywords);
                }

                if (HOP(scope.mangled, name)) {
                        return scope.mangled[name];
                } else {
                        return name;
                }
        };

        function is_name_referenced(name, ast, shallow) {
                var is_referenced = { };

                var stop_descending = [ ];

                function maybe_new_scope() {
                        if (shallow) {
                                return stop_descending;
                        }
                };

                try {
                        var subw = ast_walker();
                        subw.with_walkers({
                                "name": function(curName) {
                                        if (curName == name) {
                                                throw is_referenced;
                                        }
                                },
                                "function": maybe_new_scope,
                                "defun": maybe_new_scope,
                                "method": maybe_new_scope,
                                "class": maybe_new_scope
                        }, function() {
                                return subw.walk(ast);
                        });
                } catch (e) {
                        if (e == is_referenced) {
                                return true;
                        }

                        throw e;
                }

                return false;
        };

        function with_scope(s, cont) {
                var _scope = scope;
                scope = s;
                var ret = cont();
                ret.scope = s;
                scope = _scope;
                return ret;
        };

        function get_slice_node(target, start, count) {
                // [ ].slice.call
                var arraySliceCallNode = [ "dot", [ "array", [] ], "slice", "call" ];

                // (arguments, args ...)
                var sliceArgumentsNodes = [ [ "name", "arguments" ] ];

                if (start || count) {
                        sliceArgumentsNodes.push(start || [ "num", 0 ]);

                        if (count) {
                                sliceArgumentsNodes.push(count);
                        }
                }

                return [ "call", arraySliceCallNode, sliceArgumentsNodes ];
        };

        function rewrite_argument_definitions(args) {
            var hiddenArgs = [ ];
            var newArgNames = [ ];
            var statements = [ ];

            args.forEach(function (arg, index){
                    var argName = get_rewritten(arg[0], true);
                    var argType = arg[1];
                    var argValue = arg[2];

                    newArgNames.push(argName);

                    if (argValue == null) {
                            // Plain ol' argument
                            return;
                    }

                    var isSplat = argValue[0] == "splat";

                    if (isSplat) {
                            // Splat argument
                            // TODO Handle not-in-last-slot

                            var splatValueNode = get_slice_node([ "name", "arguments" ], [ "num", index ]);

                            // var argName = splatValueNode
                            statement = [ "var", [ [ argName, null, splatValueNode ] ] ];

                            hiddenArgs[index] = true;

                            statements.unshift(statement);
                    } else {
                            // Optional argument
                            // TODO Check for non-constant expressions

                            // arguments.length
                            var argumentsLengthNode = [ "dot", [ "name", "arguments" ], "length" ];

                            // argumentsLength < index + 1
                            var argumentOmittedNode = [ "binary", "<", argumentsLengthNode, [ "num", index + 1 ] ]

                            // argName = argValue
                            var setDefaultValueNode = [ "assign", "", [ "name", argName ], argValue ];

                            // if (argumentOmitted) setDefaultValue;
                            statements.unshift([ "stat",
                                    [ "if", argumentOmittedNode, setDefaultValueNode, null ]
                            ]);
                    }
            });

            // Strip hidden args if and only if they
            // are at the end of the argument list
            for (var i = newArgNames.length; i --> 0; ) {
                    if (hiddenArgs[i]) {
                            newArgNames = newArgNames.slice(0, i);
                    } else {
                            break;
                    }
            }

            return {
                    names: newArgNames,
                    statements: statements
            };
        };

        function _lambda(name, args, body, returnType, modifiers) {
                var is_defun = this[0] == "defun";
                var is_method = this[0] == "method";
                if (is_defun && name) name = get_rewritten(name, true, is_method);

                var argNames;

                body = with_scope(body.scope, function(){
                        scope.modifiers = modifiers;

                        if (!is_defun && name) name = get_rewritten(name, true, is_method);

                        // Rewrite arguments to be an array, if
                        // used. This must be before rewriting
                        // arguments because a rewritten argument
                        // may reference arguments, and we shouldn't
                        // need to rewrite those. (We may in the
                        // future as a size optimization, but let's
                        // keep things as simple as possible for
                        // now...)
                        var argumentsUsed = body.some(function (statement) {
                                return is_name_referenced("arguments", statement, true);
                        });

                        if (argumentsUsed) {
                                var argumentsArrayName = scope.introduce_name('args');

                                // HACK hack D=
                                scope.rev_mangled[argumentsArrayName] = "arguments";
                                scope.mangled["arguments"] = argumentsArrayName;
                        }

                        // Rewrite argument definitions
                        // (optional and splat)
                        var t = rewrite_argument_definitions(args);
                        argNames = t.names;
                        body = body.concat(t.statements);

                        body = MAP(body, walk);

                        // This must be after rewriting body because
                        // the "arguments" declared here would be
                        // rewritten (and we don't want that).
                        if (argumentsUsed) {
                                body.unshift([ "var", [
                                        // Note: no typing information
                                        [ argumentsArrayName, [ "call",
                                                sp_ref("args"),
                                                [ [ "name", "arguments" ] ]
                                        ] ]
                                ] ]);
                        }

                        return body;
                });

                return [ this[0], name, argNames, body ];
        };

        function get_sp_node() {
                return [ "name", "sp" ];
        };

        function get_this_node() {
                return [ "name", "self" ];
        };

        function sp_ref(/* ... */) {
                return [ "dot", get_sp_node() ].concat(slice(arguments));
        };

        return w.with_walkers({
                "class": function(name, members) {
                        var classCreateNode = [ "dot", [ "name", "Class" ], "create" ];
                        var bodyChildrenNodes = [ ];

                        var constructorNode = null;
                        var methods = [ ];

                        // Sort the class members by type
                        members.forEach(function(member) {
                                switch (member[0]) {
                                    case "method":
                                        if (member[1] == name) {
                                                constructorNode = walk(member);
                                        } else {
                                                var methodFunc = walk(member);

                                                methods.push([ methodFunc[1], methodFunc ]);
                                        }

                                        break;

                                    case "property":
                                        // TODO
                                        break;
                                }
                        });

                        if (constructorNode != null) {
                                bodyChildrenNodes.push([ "constructor", constructorNode ]);
                        }

                        if (methods.length > 0) {
                                bodyChildrenNodes.push([ "methods", [ "object", methods ] ]);
                        }

                        // TODO Inheritence, interfaces, etc.
                        var bodyNode = [ "object", bodyChildrenNodes ];
                        var madeClassNode = [ "call", classCreateNode, [ bodyNode ] ];

                        return [ "var", [ [ name, madeClassNode ] ] ];
                },
                "method": function(name, args, body, returnType, modifiers) {
                        var lambda = _lambda.call(this, name, args, body, returnType, modifiers);

                        body = lambda[3];

                        // Is self referenced in function body somewhere?
                        var uses_this = false;

                        var uses_this = body.some(function(statement) {
                                return is_name_referenced("self", statement);
                        });

                        // Self is referenced; include declaration
                        if (uses_this) {
                                body = [ [ "var", [
                                        [ "self", [ "atom", "this" ] ]
                                ] ] ].concat(body);
                        }

                        return [ "function", lambda[1], lambda[2], body ];
                },
                "var": function(defs) {
                        return [ this[0], MAP(defs, function(def){
                                // def = [ name, type, expr? ]
                                // Return just name and optional value
                                var a = [ get_rewritten(def[0], true) ];
                                if (def.length > 2)
                                        a[1] = walk(def[2]);
                                return a;
                        }) ];
                },
                "function": _lambda,
                "defun": _lambda,
                "name": function(name) {
                        var nameScope = scope.has(name);
                        name = get_rewritten(name);

                        // Rewrite implicit this. calls
                        if (nameScope && nameScope.is_class) {
                                return ast_add_scope_as3([ "dot", get_this_node(), name ], true);
                        } else {
                                return [ "name", name ];
                        }
                },
                "toplevel": function(body) {
                        var self = this;
                        return with_scope(self.scope, function(){
                                return [ self[0], MAP(body, walk) ];
                        });
                },
                "binary": function(operator, lvalue, rvalue) {
                        if (operator == "is") {
                                return [ "call", sp_ref("is"), [ walk(lvalue), walk(rvalue) ] ];
                        }

                        if (operator == "as") {
                                return [ "call", sp_ref("as"), [ walk(lvalue), walk(rvalue) ] ];
                        }
                }
        }, function() {
                return walk(ast_add_scope_as3(ast));
        });
};

